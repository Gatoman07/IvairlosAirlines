    public void setTblResults(ArrayList<Reserva> reservas) {
        String[] headers = {"idReserva", "Clase", "idPasajero", "Nombre Completo", "idVuelo", "Origen", "Destino", "Fecha"};
        this.tblResults.removeAll();
        DefaultTableModel tableModel = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int fila, int columna) {
                return false;
            }
        };
        tableModel.setColumnIdentifiers(headers);
        
        this.tblResults.setModel(tableModel);
        for(int i=0; i<reservas.size(); i++){
            tableModel.addRow(reservas.get(i).toArray());
        }
        tblResults.getColumnModel().getColumn(3).setPreferredWidth(200);
        tblResults.getColumnModel().getColumn(4).setPreferredWidth(60);
        tblResults.getColumnModel().getColumn(5).setPreferredWidth(110);
        tblResults.getColumnModel().getColumn(6).setPreferredWidth(110);
        
        
    }
    
    

