    private void deleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteBtnMouseClicked
        
        int filaSel = tblResults.getSelectedRow();
        
        if(filaSel==-1){
            JOptionPane.showMessageDialog(null, "No ha seleccionado ninguna reserva para eliminar");
            return;
        }
        
        if(new InitialData().deleteReserva(tblResults.getValueAt(filaSel, 0).toString())>0){
            DefaultTableModel model = (DefaultTableModel) this.tblResults.getModel();
            model.removeRow(filaSel);
        }
        
    }//GEN-LAST:event_deleteBtnMouseClicked

