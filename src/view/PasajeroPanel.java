    private int comprobarCampos(){
        
        if(nombresTF.getText().trim().equals("")) return 2;
        if(apellidosTF.getText().trim().equals("")) return 3;
        if(edadTF.getText().trim().equals("")) return 4;
        try{
            if(Integer.parseInt(edadTF.getText())>130) return 5;
        }catch(Exception ex){
            return 6; 
        }
        return 1;
        
    }
    
