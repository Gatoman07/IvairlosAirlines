    private void edadTFKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_edadTFKeyTyped
        char caracter = evt.getKeyChar();

        // Verificar si la tecla pulsada no es un digito
        if(((caracter < '0') ||
           (caracter > '9')) &&
           (caracter != '\b' /*corresponde a BACK_SPACE*/))
        {
           evt.consume();  // ignorar el evento de teclado
        }else if (edadTF.getText().length() == 3){
            evt.consume();
        }
    }//GEN-LAST:event_edadTFKeyTyped

    private void registrarBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_registrarBtnMouseClicked
        
        int correct = comprobarCampos();
        
        if(correct==1){
            
            int confirm = JOptionPane.showConfirmDialog(this, "¿Desea registrar al usuario:"
                    + "\n"+nombresTF.getText()+" "+apellidosTF.getText()+"\nCon identificacion: "+idTF.getText());
            
            if(confirm==0){
                initialData.crearPasajero(idTF.getText(), nombresTF.getText(), apellidosTF.getText(), edadTF.getText(), generoCbx.getSelectedItem().toString());
                ReservaPanel.finalizarRegistro();
                principal.dispose();
                new MainWindow();
            }
            
        }else{
            switch(correct){
                case 2: JOptionPane.showMessageDialog(null, "Ingrese sus nombres");     break;
                case 3: JOptionPane.showMessageDialog(null, "Ingrese sus apellidos");   break;
                case 4: JOptionPane.showMessageDialog(null, "Ingrese su edad");         break;
                default: JOptionPane.showMessageDialog(null, "Edad no válida");
            }
        }
        
    }//GEN-LAST:event_registrarBtnMouseClicked

